public class Encapsulation  {
	
	private String firstName;
	private String lastName;

	public String getFirstName(){ 
		return firstName;
	}
	public void setFirstName(String fn) {
		this.firstName = fn;
	}

	public String getLastName(){ 
		return lastName;
	}
	public void setLastName(String fn) {
		this.lastName = fn;
	}

}